// se utiliza para comparar distintas variables entre si, o una variable con otra cosa

let a = 10;

// operadores relacionales
// se hace la compracion, comparando si a es mayor que 5, nos devuelve true,
console.log(a > 5);
// operado >= mayor o igual que,
console.log(a >= 5);

console.log(a < 5);
console.log(a <= 5);

// operadores de igualdad
// es igual
console.log(a == 10);
// es diferente con el simbolo de esclamasion !
console.log(a != 10);
console.log(a == '10');
// es mejor utilizarlos de esta manera los de abajo
// con la triple igualdad, consulta si tiene el mismo valor, tiene el mismo tipo de dato, 
console.log(a === '10');
// preguntar si son distintos tipo de datos o de valor
console.log(a !== '10');